number_list = [123,125,164,125.15]
name_list = ['Hello World','Python']

print (number_list)             #   Prints only number list
print (name_list)               #   Prints only name list
print (number_list[0])          #   Prints first element of the number list
print (number_list[1:3])        #   Prints element starting from 2nd till 3rd
print (name_list * 2)           #   Prints name list two times
print (number_list + name_list) #   Prints concatenated lists